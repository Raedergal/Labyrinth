function generateMaze()
{
    $randRow = rand(5, 7);
    $randCol = rand(5, 7);
    $array = array();
    $arrayRow = array();

    for ($i = 0; $i < $randRow; $i++) {
        for ($j = 0; $j < $randCol; $j++) {
            $arrayRow[$j] = "w";
        }
        $array[$i] = $arrayRow;
    }
    $array[rand(0, ($randRow)-1)][rand(0, ($randCol)-1)] = "b";
    return $array;
}
